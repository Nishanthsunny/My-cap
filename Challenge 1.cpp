#include<iostream>
using namespace std;

int main(){
	int a;
	float b;
	char c;
	double d;
	cout<<"Size of char: "<<sizeof(c)<< " byte\n";
	cout<<"Size of int: "<<sizeof(b)<< " bytes\n";
	cout<<"Size of float: "<<sizeof(a)<< " bytes\n";
	cout<<"Size of double: "<<sizeof(d)<< " bytes\n";
	
	return 0;
}
